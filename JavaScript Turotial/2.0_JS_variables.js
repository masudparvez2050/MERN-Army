// JavaScript Variables

// javaScript Variables can be declared in 4 ways:

/* 
1. Automatically
2. using "var"
3. using "let"
4. using "const"

*/

// 1. Automatically assigned a value
x = 5;
y = 6;
z = x + y;

console.log(z);

// 2. Using "var"

var x = 5;
var y = 6;
var z = x + y;

console.log(z);

// 3. Using "let"

let a = 5;
let b = 6;
let c = a + b;

console.log(z);

// 4. Using "const"

const d = 5;
const e = 6;
const f = d + e;

console.log(f);

// Mixed Example

let g = 5;
const h = 6;

g = g + h;
console.log(g); // Outputs: 11
